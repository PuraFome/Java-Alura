public class TestaCondicional {

    public static void main(String[] args) {

        System.out.println("Testando Condicionais");

        int idade = 10;
        
        if( idade >= 18 ) {

            System.out.println("Você é maior de idade, tem: " + idade);
        } else {
            System.out.println("Você é menor de idade, tem: " + idade);
        }	
        


    }
    
}
